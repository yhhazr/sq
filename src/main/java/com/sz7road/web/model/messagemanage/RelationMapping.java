package com.sz7road.web.model.messagemanage;

public class RelationMapping {
	public String mapTableName;
	public String mapTitleColName;
	public String mapIdColName;
	
	
	public RelationMapping(){
		
	}


	public String getMapTableName() {
		return mapTableName;
	}


	public void setMapTableName(String mapTableName) {
		this.mapTableName = mapTableName;
	}


	public String getMapTitleColName() {
		return mapTitleColName;
	}


	public void setMapTitleColName(String mapTitleColName) {
		this.mapTitleColName = mapTitleColName;
	}


	public String getMapIdColName() {
		return mapIdColName;
	}


	public void setMapIdColName(String mapIdColName) {
		this.mapIdColName = mapIdColName;
	}


	
}
