package com.sz7road.web.model.gamemanage;

public class GameType {
	private int typeId;
	private String typeName;
	//getter and setter
	public int getTypeId() {
		return typeId;
	}
	public void setTypeId(int typeId) {
		this.typeId = typeId;
	}
	public String getTypeName() {
		return typeName;
	}
	public void setTypeName(String name) {
		this.typeName = name;
	}
}
