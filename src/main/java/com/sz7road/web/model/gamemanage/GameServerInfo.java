package com.sz7road.web.model.gamemanage;



public class GameServerInfo{
		private String serverId;
		private String serverNo;
		private String serverName;
		private String mainServerId;
		private String mainServerNo;
		public String getServerId() {
			return serverId;
		}
		public void setServerId(String serverId) {
			this.serverId = serverId;
		}
		public String getServerNo() {
			return serverNo;
		}
		public void setServerNo(String serverNo) {
			this.serverNo = serverNo;
		}
		public String getServerName() {
			return serverName;
		}
		public void setServerName(String serverName) {
			this.serverName = serverName;
		}
		public String getMainServerId() {
			return mainServerId;
		}
		public void setMainServerId(String mainServerId) {
			this.mainServerId = mainServerId;
		}
		public String getMainServerNo() {
			return mainServerNo;
		}
		public void setMainServerNo(String mainServerNo) {
			this.mainServerNo = mainServerNo;
		}
		
	}
