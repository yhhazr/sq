package com.sz7road.web.model.newsmanage;

public class NewsType {

	private int newsTypeId;
	
	private String newsTypeName;
	
	private String newsTypeShortName;

	public int getNewsTypeId() {
		return newsTypeId;
	}

	public void setNewsTypeId(int newsTypeId) {
		this.newsTypeId = newsTypeId;
	}

	public String getNewsTypeName() {
		return newsTypeName;
	}

	public void setNewsTypeName(String newsTypeName) {
		this.newsTypeName = newsTypeName;
	}

	public String getNewsTypeShortName() {
		return newsTypeShortName;
	}

	public void setNewsTypeShortName(String newsTypeShortName) {
		this.newsTypeShortName = newsTypeShortName;
	}
	
	
}
